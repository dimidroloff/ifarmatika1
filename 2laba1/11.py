# 11. Пусть А(x1,y1), B(x2,y2), тогда расстояние AB=ඥ(𝑥2 − 𝑥1)ଶ + (𝑦2 − 𝑦1)ଶ ФОРМУЛИРОВКА
import math
a = input("Введите первые координаты (вид - x, y): ")
x1 = int(a.split(", ")[0])
y1 = int(a.split(", ")[1])
b = input("Введите вторые координаты: ")
x2 = int(b.split(", ")[0])
y2 = int(b.split(", ")[1])

ab = math.sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2)
print("Длина -", ab)
